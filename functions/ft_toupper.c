#include "libft.h"

void	ft_toupper(char c)
{
	if(ft_isalpha(c))
	{
		if(c < 'a' && c > 'z')
			c -= 32;
		else if (c < 'A' && c > 'Z')
			c += 32;
	}
	else
		return (0);
}
